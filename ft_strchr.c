char	*ft_strchr(const char *s, int c)
{
	int	i;
	
	i = 1;
	if (s[0] == c)
		return ((char *)s);
	while (s[i]-1 != '\0')
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	return (0);
}
